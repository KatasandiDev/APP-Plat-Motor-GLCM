    private void txtnimKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnimKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==10){
            try{
                koneksi();
                String sql="Select * from tblpemilik where " +
                "nim='" + txtnim.getText() + "'";
                ResultSet rs=cn.executeQuery(sql);
                if(rs.next()){
                    txtnma.setText(rs.getString("nama"));
                    cb_jk.addItem(rs.getString("jeniskelamin"));
                    txtanktn.setText(rs.getString(3));
                    txtalmt.setText(rs.getString("alamat"));
                    txtplat.setText(rs.getString("noplat"));
                    txtnim.setEnabled(false);
                    txtnma.setEnabled(true);
                    txtplat.setEnabled(true);
                    txtanktn.setEnabled(true);
                    cb_jk.setEnabled(true);
                    txtalmt.setEnabled(true);
                    btnsimpan.setEnabled(true);
                    btnsimpan.setText("Update");
                    btnhapus.setEnabled(true);
                    x=1;
                }else{
                    txtnma.setText("");
                    txtanktn.setText("");
                    txtalmt.setText("");
                    txtplat.setText("");
                    txtnma.setEnabled(true);
                    txtanktn.setEnabled(true);
                    txtplat.setEnabled(true);
                    cb_jk.setEnabled(true);
                    txtalmt.setEnabled(true);
                    txtnma.requestFocus();
                    btnsimpan.setEnabled(true);
                    btnsimpan.setText("Simpan");
                    btnhapus.setEnabled(false);
                    x=0;
                }
            }catch (Exception e){}
        }
    }//GEN-LAST:event_txtnimKeyPressed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        if (x==0){
            try{
                koneksi();
                String perintahSQL="INSERT INTO tblpemilik values ("
                + "'" + txtnim.getText() + "',"
                + "'" + txtnma.getText() + "',"
                + "'" + txtanktn.getText() + "',"
                + "'" + cb_jk.getSelectedItem() + "',"
                + "'" + txtalmt.getText() + "',"
                + "'" + txtplat.getText() + "'"
                + ")";
                cn.executeUpdate(perintahSQL);
                conn.close();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                tampildata();
                normal();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Maaf, " +
                    "Proses penyimpanan data gagal");
            }
        }else{
            try{
                koneksi();
                String perintahSQL="update tblpemilik set"
                + " nama = '" + txtnma.getText() + "',"
                + " angkatan = '" + txtanktn.getText() + "',"
                + " jeniskelamin = '" + cb_jk.getSelectedItem() + "',"
                + " alamat = '" + txtalmt.getText() + "',"
                + " noplat = '" + txtplat.getText() + "' where"
                + " nim = '" + txtnim.getText() +  "'";

                cn.executeUpdate(perintahSQL);
                conn.close();
                JOptionPane.showMessageDialog(null, "Proses " +
                    "Update berhasil");
                tampildata();
                normal();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Maaf, " +
                    "Proses Update data gagal");
            }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

