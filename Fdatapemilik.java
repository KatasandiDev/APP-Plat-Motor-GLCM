    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String[] Opsi = {"Ya'","Tidak'"};
        int Pilih = JOptionPane.showOptionDialog(null,"Yakin Anda akan " +
            "menghapus data ini...?",
            "Pertanyaan",JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,null,Opsi,Opsi[0]);
        if(Pilih == JOptionPane.YES_OPTION){
            try{
                koneksi();
                String perintahSQL="DELETE FROM tblpemilik WHERE " +
                "nim='" + txtnim.getText() + "'";
                cn.executeUpdate(perintahSQL);
                conn.close();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                tampildata();
                normal();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Maaf, " +
                    "Proses penghapusan data gagal");
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

